function add(a, b)
{
    let result;
    result = a + b;
    return result;
}

function subtract(a, b)
{
    let result;
    result = a - b;
    return result;
}

function multiply(a, b)
{
    let result;
    result = a * b;
    return result;
}

function divide(a, b)
{
    let result;
    result = a / b;
    return result;
}

function increment(n)
{
    let result;
    result = ++n;
    return result;
}

function decrement(n)
{
    let result;
    result = --n;
    return result;
}

function makeInt(n)
{
    let result;
    result = parseInt(n, 10);
    return result;
}

function preserveDecimal(n)
{
    let result;
    result = parseFloat(n);
    return result;
}